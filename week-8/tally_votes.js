// Initial Solution


for (var key in votes) 
  if(votes.hasOwnProperty(key)) {
       var obj = votes[key];
        for (var prop in obj) {
        
          // important check that this is objects own property 
          // not from prototype prop inherited
          
            if(voteCount[prop].hasOwnProperty(votes[key][prop])){
            voteCount[prop][votes[key][prop]] ++;
            }
            else {
              voteCount[prop][votes[key][prop]] = 1;
            } 
          } 
       }

function nominate(){
  var count = 0;
  for (var key in voteCount){
    if(voteCount.hasOwnProperty(key)) {
      
      for (var name in voteCount[key]){
      if (count < voteCount[key][name]) {
        count = voteCount[key][name];
        officers[key] = name;
      }
        
      }
      
    }
    count = 0;
  }
  return officers;
}
nominate();

